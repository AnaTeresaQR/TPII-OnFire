    private void jB_loginUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_loginUserActionPerformed
        initVariables();

        try {
            controller.processConfirmationAction(true);
            int resultLogin = controller.loginUser(email, password);
            if (resultLogin == 1) {
                JOptionPane.showMessageDialog(null, "Resultado de Registrar\n" + "Bienvenido al programa");
                controller.setEmailSession(email);
                controller.showMenu(controller);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Resultado de Registrar\n" + "No se ha logrado el login");
                controller.returnView(controller, this);
            }
        } catch (IOException e) {
            System.out.println(e.getMessage());
        }


    }//GEN-LAST:event_jB_loginUserActionPerformed

    private void jB_returnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_returnViewActionPerformed
        try {
            controller.processConfirmationAction(false);
            controller.returnView(controller);
            this.dispose();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jB_returnViewActionPerformed

